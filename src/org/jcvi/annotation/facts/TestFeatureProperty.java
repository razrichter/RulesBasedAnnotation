package org.jcvi.annotation.facts;

public class TestFeatureProperty {
	private String id;
	private double value;
	
	public TestFeatureProperty(String id) {
		super();
		this.id = id;
	}
	public String getid() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public double getValue() {
		return value;
	}
	public void setValue(double value) {
		this.value = value;
	}
	
}
